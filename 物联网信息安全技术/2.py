# 辅助函数: 字符串/字节流 与 二进制数组 的转换
# ==========================================
def string_to_bits(s):
    bits = []
    for char in s.encode('utf-8'):
        bits.extend([int(x) for x in format(char, '08b')])
    # 填充到64的整数倍 (简单的补零填充，实际应用多用PKCS7)
    while len(bits) % 64 != 0:
        bits.append(0)
    return bits

def bits_to_string(bits):
    chars = []
    for i in range(0, len(bits), 8):
        byte = bits[i:i+8]
        val = int("".join(str(b) for b in byte), 2)
        if val != 0: # 剔除补全的空字符
            chars.append(chr(val))
    return "".join(chars)


