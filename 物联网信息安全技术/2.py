# ==========================================
# 基础工具函数
# ==========================================
def permute(block, table):
    """根据置换表进行位重新排列"""
    return [block[i - 1] for i in table]

def shift_left(block, n):
    """循环左移"""
    return block[n:] + block[:n]

def xor(b1, b2):
    """按位异或"""
    return [x ^ y for x, y in zip(b1, b2)]


# ==========================================
# DES 核心模块实现
# ==========================================
def generate_subkeys(key_64):
    """子密钥产生模块：从64位主密钥生成16个48位子密钥"""
    subkeys = []
    # 1. PC-1 压缩置换 (64 -> 56)
    key_56 = permute(key_64, PC1)
    
    # 分割为左右两部分 (28位)
    C, D = key_56[:28], key_56[28:]
    
    for shift in SHIFTS:
        # 2. 循环左移
        C = shift_left(C, shift)
        D = shift_left(D, shift)
        # 3. PC-2 压缩置换 (56 -> 48)
        subkey = permute(C + D, PC2)
        subkeys.append(subkey)
        
    return subkeys

def feistel_function(R_32, subkey_48):
    """轮函数 F: 包含扩展、异或、S盒代换和P盒置换"""
    # 1. 扩展模块 E盒 (32 -> 48)
    expanded = permute(R_32, E_BOX)
    
    # 2. 与子密钥异或
    xored = xor(expanded, subkey_48)
    
    # 3. 压缩模块 S盒 (48 -> 32)
    s_box_out = []
    for i in range(8):
        chunk = xored[i*6 : (i+1)*6]
        # 行由第1位和第6位决定
        row = (chunk[0] << 1) + chunk[5]
        # 列由中间4位决定
        col = (chunk[1] << 3) + (chunk[2] << 2) + (chunk[3] << 1) + chunk[4]
        
        # 查对应的S盒
        val = S_BOX[i][row][col]
        # 转为4位二进制数组并追加
        s_box_out.extend([int(x) for x in format(val, '04b')])
        
    # 4. 置换模块 P盒 (32 -> 32)
    return permute(s_box_out, P_BOX)

def des_block(input_64, key_64, mode='encrypt'):
    """DES 主流程，处理64位(一个分组)的加解密"""
    # 1. 初始置换 IP
    data = permute(input_64, IP)
    L, R = data[:32], data[32:]
    
    # 生成子密钥
    subkeys = generate_subkeys(key_64)
    # 解密时，子密钥逆序使用
    if mode == 'decrypt':
        subkeys = subkeys[::-1]
        
    # 2. 16轮Feistel���络运算
    for i in range(16):
        next_L = R
        # R(i) = L(i-1) ⊕ F(R(i-1), K_i)
        f_res = feistel_function(R, subkeys[i])
        next_R = xor(L, f_res)
        L, R = next_L, next_R
        
    # 3. 左右交换 (最后一轮完毕后，合并为 R + L)
    combined = R + L
    
    # 4. 逆初始置换 IP_INV
    return permute(combined, IP_INV)


